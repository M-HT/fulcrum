void DrawGfxCharacter32BitASM(uint8_t *pcString, int iBackBufferPos, void *pBackBuffer, int iXmax)
{
    uint8_t ch = *pcString;

    if (ch == 0) return;

    if ((ch < 0x20) || (ch > 0x7e))
    {
        ch = 0x20;
    }

    const uint8_t *chptr = &(char20[ch * CharacterSize - CharacterFirstOffset]);

    uint32_t *dst = (uint32_t *) (iBackBufferPos + (uintptr_t)pBackBuffer);

    for (int i = 8; i != 0; i--)
    {
        for (int j = 8; j != 0; j--)
        {
            if (*chptr != 0)
            {
                *dst = 0x00ffffff;
            }
            dst++;
            chptr++;
        }

        dst += (iXmax - 8);
    }
}

